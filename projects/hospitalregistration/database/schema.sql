-- 安康綜合醫院 掛號系統 SQLite Schema
-- Referenced from Hospital Registration UI

PRAGMA foreign_keys = ON;

-- ==============================
-- 縣市表
-- ==============================
CREATE TABLE IF NOT EXISTS cities (
    name TEXT PRIMARY KEY  -- 縣市名稱
);

-- ==============================
-- 區域表 (關聯縣市)
-- ==============================
CREATE TABLE IF NOT EXISTS districts (
    city_name TEXT NOT NULL,      -- 所屬縣市
    name      TEXT NOT NULL,      -- 區域名稱
    PRIMARY KEY (city_name, name),
    FOREIGN KEY (city_name) REFERENCES cities(name)
);

-- ==============================
-- 科別表
-- ==============================
CREATE TABLE IF NOT EXISTS departments (
    code TEXT PRIMARY KEY,  -- 科別代碼
    name TEXT NOT NULL      -- 科別名稱
);

-- ==============================
-- 醫師表 (關聯科別)
-- ==============================
CREATE TABLE IF NOT EXISTS doctors (
    department_code TEXT NOT NULL,  -- 所屬科別代碼
    name            TEXT NOT NULL,  -- 醫師姓名
    PRIMARY KEY (department_code, name),
    FOREIGN KEY (department_code) REFERENCES departments(code)
);

-- ==============================
-- 病患資料表 (關聯縣市、區域)
-- ==============================
CREATE TABLE IF NOT EXISTS patients (
    medical_no    TEXT PRIMARY KEY,  -- 病歷號碼
    name          TEXT NOT NULL,     -- 姓名
    gender        TEXT CHECK(gender IN ('male', 'female')),
    birthday      TEXT,              -- YYYY/MM/DD
    age           INTEGER,
    phone         TEXT,
    city_name     TEXT,              -- 縣市
    district_name TEXT,              -- 區域
    address       TEXT,              -- 詳細地址
    created_at    TEXT DEFAULT (datetime('now', 'localtime')),
    updated_at    TEXT DEFAULT (datetime('now', 'localtime')),
    FOREIGN KEY (city_name) REFERENCES cities(name),
    FOREIGN KEY (city_name, district_name) REFERENCES districts(city_name, name)
);

-- ==============================
-- 掛號紀錄表
-- ==============================
CREATE TABLE IF NOT EXISTS registrations (
    reg_date        TEXT NOT NULL,
    reg_period      TEXT NOT NULL CHECK(reg_period IN ('morning', 'afternoon')),
    department_code TEXT NOT NULL,
    reg_number      TEXT NOT NULL,
    patient_medical_no TEXT NOT NULL,
    visit_type      TEXT CHECK(visit_type IN ('first', 'return')),
    doctor_name     TEXT NOT NULL,
    status          TEXT DEFAULT 'confirmed' CHECK(status IN ('confirmed', 'cancelled', 'completed')),
    created_at      TEXT DEFAULT (datetime('now', 'localtime')),
    PRIMARY KEY (reg_date, reg_period, department_code, reg_number),
    FOREIGN KEY (patient_medical_no) REFERENCES patients(medical_no),
    FOREIGN KEY (department_code) REFERENCES departments(code),
    FOREIGN KEY (department_code, doctor_name) REFERENCES doctors(department_code, name)
);

-- ==============================
-- 索引
-- ==============================
CREATE INDEX idx_districts_city         ON districts(city_name);
CREATE INDEX idx_doctors_department     ON doctors(department_code);
CREATE INDEX idx_patients_city          ON patients(city_name);
CREATE INDEX idx_patients_district      ON patients(city_name, district_name);
CREATE INDEX idx_registrations_patient  ON registrations(patient_medical_no);

